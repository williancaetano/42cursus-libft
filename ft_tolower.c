#include "libft.h"

int	ft_tolower(int c)
{
	if (c > 255 || c == EOF)
		return (c);
	if ((unsigned char) c >= 'A' && (unsigned char) c <= 'Z')
		return ((unsigned char) c + 'a' - 'A');
	return ((unsigned char) c);
}
